package fundamentos

fun main() {
    // Valores máximos Inteiros
    val num1: Byte = 127
    val num2: Short = 32767
    val num3: Int = 2_147_483_647
    val num4: Long = 9_223_372_036_854_807

    // Valores numéricos Reais
    val num5: Float = 3.14F
    val num6: Double = 3.14

    // Tipo caractere
    val char: Char = '!'

    // Tipo booleano
    val booleano: Boolean = true // ou false

    println(listOf(num1, num2, num3, num4, num5, num6, char, booleano))
}